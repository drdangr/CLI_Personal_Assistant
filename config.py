"""
Конфигурация приложения «Персональный помощник»
"""

# Приложение
APP_NAME = "personal_assistant_cli"
APP_VERSION = "1.0.0"

# Валидация контактов
PHONE_DIGITS = 10
# Паттерн для телефона: ^ (начало) + ровно 10 цифр + $ (конец)
# Примеры: '1234567890' ✓, '123' ✗, '12345678901' ✗
PHONE_REGEX = r"^\d{10}$"

# Паттерн для email: имя@домен.тип
# Примеры: 'user@example.com' ✓, 'invalid@.com' ✗, 'no-at-sign.com' ✗
# Компоненты:
#   - Начало: буквы, цифры, точка, %, +, -
#   - @: обязательный символ
#   - Домен: буквы, цифры, точка, дефис
#   - TLD: минимум 2 буквы после последней точки
EMAIL_REGEX = r"^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$"

# Формат даты рождения: день.месяц.год (DD.MM.YYYY)
# Примеры: '15.03.1990' ✓, '15-03-1990' ✗, '1990.03.15' ✗
BIRTHDAY_FORMAT = "%d.%m.%Y"

# Дни рождения по умолчанию
UPCOMING_DAYS_DEFAULT = 7

# Форматирование вывода
SEPARATOR = "\n\n---\n\n"
