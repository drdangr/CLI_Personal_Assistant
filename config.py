"""
Конфігурація застосунку «Персональний помічник»
"""

# Застосунок
APP_NAME = "personal_assistant_cli"
APP_VERSION = "1.1.0"

# Валідація контактів
PHONE_DIGITS = 10
# Патерн для телефону: ^ (початок) + рівно 10 цифр + $ (кінець)
# Приклади: '1234567890' ✓, '123' ✗, '12345678901' ✗
PHONE_REGEX = r"^\d{10}$"

# Патерн для email: ім'я@домен.тип
# Приклади: 'user@example.com' ✓, 'invalid@.com' ✗, 'no-at-sign.com' ✗
# Компоненти:
#   - Початок: літери, цифри, крапка, %, +, -
#   - @: обов'язковий символ
#   - Домен: літери, цифри, крапка, дефіс
#   - TLD: мінімум 2 літери після останньої крапки
EMAIL_REGEX = r"^[A-Za-z0-9._%+-]+@[A-Za-z0-9.-]+\.[A-Za-z]{2,}$"

# Формат дати народження: день.місяць.рік (DD.MM.YYYY)
# Приклади: '15.03.1990' ✓, '15-03-1990' ✗, '1990.03.15' ✗
BIRTHDAY_FORMAT = "%d.%m.%Y"

# Дні народження за замовчуванням
UPCOMING_DAYS_DEFAULT = 7

# Форматування виводу
SEPARATOR = "\n\n---\n\n"
